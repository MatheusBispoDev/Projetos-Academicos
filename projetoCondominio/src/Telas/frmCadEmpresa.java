    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if("".equals(txtCodigo.getText())||"".equals(fxtCnpj.getText())||"".equals(txtNome.getText())||
            "".equals(txtRazao.getText())||"".equals(fxtLocador.getText())){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else{
            Empresa emp = new Empresa();
            EmpresaDAO dao = new EmpresaDAO();

            emp.setCodigo(Integer.parseInt(txtCodigo.getText()));
            emp.setCnpj(fxtCnpj.getText());
            emp.setNome_Fantasia(txtNome.getText());
            emp.setRazao_Social(txtRazao.getText());
            emp.setLocador(fxtLocador.getText());

            dao.update(emp);
            limpar();
            readJTable();
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

