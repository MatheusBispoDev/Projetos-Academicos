    private void limpar(){
        txtCodigo.setText("");
        fxtCnpj.setText("");
        txtNome.setText("");       
        txtRazao.setText("");
        fxtLocador.setText("");
        btnCadastrar.setEnabled(true);
        txtCodigo.setEnabled(true);
    }
    
    private void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tblCon.getModel();
        modelo.setNumRows(0);
        EmpresaDAO edao = new EmpresaDAO();

        for (Empresa emp : edao.read()) {

            modelo.addRow(new Object[]{
                emp.getCodigo(),
                emp.getLocador(),
                emp.getNome_Fantasia(),
                emp.getRazao_Social(),                
                emp.getCnpj()
            });
        }
    }
