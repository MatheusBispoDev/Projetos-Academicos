    private void limpar(){
        txtCodigo.setText("");
        txtAndar.setText("");
        txtEstacio.setText("");
        txtSalas.setText("");
        
        txtCodigo.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnCadastrar.setEnabled(true);
    }
    
    private void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tblCon.getModel();
        modelo.setNumRows(0);
        CondominioDAO cdao = new CondominioDAO();

        for (Condominio con : cdao.read()) {

            modelo.addRow(new Object[]{
                con.getCodigo(),
                con.getEmpresa(),
                con.getAndar(),
                con.getSalas(),
                con.getVagas_Estacionamento()
            });
        }
    }
