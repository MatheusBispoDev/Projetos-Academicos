    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if("".equals(txtCodigo.getText())||"".equals(txtAndar.getText())||"".equals(txtSalas.getText())||"".equals(txtEstacio.getText())){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else{
            Condominio c = new Condominio();
            CondominioDAO dao = new CondominioDAO();
            Empresa em = (Empresa) cmbEmpresa.getSelectedItem();

            c.setCodigo(Integer.parseInt(txtCodigo.getText()));
            c.setEmpresa(em.getCodigo());
            c.setAndar(txtAndar.getText());
            c.setSalas(Integer.parseInt(txtSalas.getText()));
            c.setVagas_Estacionamento(Integer.parseInt(txtEstacio.getText()));
            dao.create(c);
            
            limpar();
            readJTable();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

