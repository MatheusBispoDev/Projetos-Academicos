    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if("".equals(txtCodigo.getText())||"".equals(txtAgua.getText())||"".equals(txtEnergia.getText())||"".equals(txtTaxa.getText())
            ||"".equals(fxtData.getText())||"".equals(txtValor.getText())){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else{
            atualiza_Total();
            Contas_Condominio cc = new Contas_Condominio();
            Contas_CondominioDAO dao = new Contas_CondominioDAO();

            cc.setCodigo(Integer.parseInt(txtCodigo.getText()));
            cc.setCondominio(Integer.parseInt(cmbCondominio.getSelectedItem().toString()));
            cc.setAgua(Double.parseDouble(txtAgua.getText()));
            cc.setEnergia(Double.parseDouble(txtEnergia.getText()));
            cc.setTaxa_Manutencao(Double.parseDouble(txtTaxa.getText()));
            cc.setData_Contas(fxtData.getText());
            cc.setValor_Condominio(Double.parseDouble(txtValor.getText()));
            cc.setTotal(Double.parseDouble(lblTotal.getText()));

            dao.create(cc);

            limpar();
            readJTable();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

