    private void limpar(){
        txtCodigo.setText("");
        txtAgua.setText("00.00");
        txtEnergia.setText("00.00");
        txtTaxa.setText("00.00");
        fxtData.setText("");
        txtValor.setText("00.00");
        lblTotal.setText("00.00");
        
        txtCodigo.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnCadastrar.setEnabled(true);
    }
    
    private void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tblCon.getModel();
        modelo.setNumRows(0);
        Contas_CondominioDAO cdao = new Contas_CondominioDAO();

        for (Contas_Condominio con : cdao.read()) {

            modelo.addRow(new Object[]{
                con.getCodigo(),
                con.getCondominio(),
                con.getAgua(),
                con.getEnergia(),
                con.getTaxa_Manutencao(),
                con.getData_Contas(),
                con.getValor_Condominio(),
                con.getTotal()
            });
        }
    }
    private void atualiza_Total(){
        double total;
        total = Double.parseDouble(txtAgua.getText()) + Double.parseDouble(txtEnergia.getText()) +
                Double.parseDouble(txtTaxa.getText()) + Double.parseDouble(txtValor.getText());
        
        lblTotal.setText(String.valueOf(total));
    }
