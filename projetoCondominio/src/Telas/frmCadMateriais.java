    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if("".equals(txtCodigo.getText())||"".equals(txtNome.getText())||"".equals(txtValor.getText())|| lblImg.getIcon()==null){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else{
            Materiais m = new Materiais();
            MaterialDAO dao = new MaterialDAO();

            m.setCodigo(Integer.parseInt(txtCodigo.getText()));
            m.setNome(txtNome.getText());
            m.setDescr(txtDescricao.getText());
            m.setValor(Double.parseDouble(txtValor.getText()));
            m.setImg(txtImg.getText());
            dao.create(m);

            txtCodigo.setText("");
            txtNome.setText("");
            txtValor.setText("");
            txtCodigo.setEnabled(true);
            txtDescricao.setText("");
            txtImg.setText("");
            lblImg.setText("Imagem");
            lblImg.setIcon(null);
            readJTable();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

