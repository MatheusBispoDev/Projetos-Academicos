    private void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tblCon.getModel();
        modelo.setNumRows(0);
        MaterialDAO mdao = new MaterialDAO();

        for (Materiais m : mdao.read()) {

            modelo.addRow(new Object[]{
                m.getCodigo(),
                m.getNome(),
                m.getValor(),
                m.getDescr(),
                m.getImg()
            });
        }
    }
