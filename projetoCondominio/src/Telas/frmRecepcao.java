    private void btnValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarActionPerformed
        VisitanteDAO vidao = new VisitanteDAO();
        FuncionarioDAO fundao = new FuncionarioDAO();
        String rg = fxtRG.getText();
        System.out.println(rg);
        if(vidao.validar(rg)){            
            Visitante vis = vidao.readRg(rg);
            lblNome.setText("Visitante: " + vis.getNome() + " RG: " + vis.getRg());
            validar_Layout("Permitido");
            cad_Recepcao();
        }
        else if(fundao.validar(rg)){                     
            Funcionario fun = fundao.readRg(rg);
            lblNome.setText("Funcion√°rio: "+ fun.getNome() + " RG: " + fun.getRg());
            validar_Layout("Permitido");
            cad_Recepcao();
        }
        else{
            validar_Layout("Negado");
        }
 
    }//GEN-LAST:event_btnValidarActionPerformed
    
