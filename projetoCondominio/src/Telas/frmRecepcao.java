    private void cad_Recepcao(){
        String rg = fxtRG.getText();
        Recepcao rec = new Recepcao();
        RecepcaoDAO recdao = new RecepcaoDAO();
        //dia
        Date d = new Date();
        SimpleDateFormat formatard = new SimpleDateFormat("dd/MM/yy");
        String data = formatard.format(d);
        //hora
        Date h = new Date();
        SimpleDateFormat formatarh = new SimpleDateFormat("HH:mm:ss");
        String hora = formatarh.format(h);
        
        rec.setCodigo(recdao.cod_read() + 1);
        rec.setDia(data);
        rec.setHora(hora);
        rec.setRg(rg);
        
        recdao.create(rec);        
    }
    private void validar_Layout(String tipo){
        switch (tipo) {
            case "Permitido":
                lblValidar.setText("Acesso Permitido");
                lblValidar.setForeground(new Color(0,204,51));
                lblNome.setForeground(new Color(0,204,51));
                btnCadFunc.setVisible(false);
                btnCadVisi.setVisible(false);
                break;
            case "Negado":
                lblValidar.setText("Acesso Negado, Rg n√£o encontrado");
                lblNome.setText("");
                lblValidar.setForeground(new Color(255,0,0));     
                btnCadFunc.setVisible(true);
                btnCadVisi.setVisible(true);
                break;
        }
    }
